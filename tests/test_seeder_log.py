def testformat_event_customer_created_monthly():
    ev = _ev("sim_1", 0, "customer_created", tier="standard", cadence="month")
    assert format_event(ev) == "customer_created (standard, monthly)"


def testformat_event_customer_created_yearly():
    ev = _ev("sim_1", 0, "customer_created", tier="engage", cadence="year")
    assert format_event(ev) == "customer_created (engage, yearly)"


def testformat_event_tier_upgraded():
    ev = _ev("sim_1", 47, "tier_upgraded", **{"from": "standard", "to": "pro_plus"})
    assert format_event(ev) == "tier_upgraded (standard → pro_plus)"


def testformat_event_tier_downgraded():
    ev = _ev("sim_1", 47, "tier_downgraded", **{"from": "pro_plus", "to": "standard"})
    assert format_event(ev) == "tier_downgraded (pro_plus → standard)"


def testformat_event_bare_status_events():
    assert format_event(_ev("sim_1", 60, "marked_past_due")) == "marked_past_due"
    assert format_event(_ev("sim_1", 70, "canceled")) == "canceled"
    assert format_event(_ev("sim_1", 80, "recovered")) == "recovered"


