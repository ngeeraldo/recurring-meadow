def test_summary_block_basic_fields():
    text = _minimal_log(seed=7, sim_days=90, initial_count=3,
                        states={f"sim_{i}": _state(f"sim_{i}") for i in (1, 2, 3, 4)})
    assert "Seed: 7" in text
    assert "Simulation days: 90" in text
    assert "Initial customers: 3" in text
    assert "Final customer count: 4" in text


def test_summary_state_distribution_sorted_alphabetically():
    text = _minimal_log(
        states={f"sim_{i}": _state(f"sim_{i}") for i in (1, 2, 3)},
        state_counts=Counter({"past_due": 1, "active": 2}),
    )
    section = text.split("Final state distribution:")[1].split("\n\n")[0]
    lines = [ln for ln in section.splitlines() if ln.strip()]
    # Sorted alphabetically: active before past_due
    assert lines[0].lstrip().startswith("active:")
    assert lines[1].lstrip().startswith("past_due:")
    assert "2" in lines[0]
    assert "1" in lines[1]


def test_summary_active_tier_distribution_with_percent():
    text = _minimal_log(
        tier_counts=Counter({"standard": 30, "pro_plus": 14, "engage": 8, "enterprise": 6}),
    )
    section = text.split("Final active-tier distribution:")[1].split("\n\n")[0]
    # 30 / 58 = 51.7%, 14/58 = 24.1%, 8/58 = 13.8%, 6/58 = 10.3%
    assert "(51.7%)" in section
    assert "(24.1%)" in section
    assert "(13.8%)" in section
    assert "(10.3%)" in section


def test_summary_active_cadence_translates_month_year_to_words():
    text = _minimal_log(
        cadence_counts=Counter({"month": 49, "year": 9}),
    )
    section = text.split("Final active-cadence distribution:")[1].split("\n\n")[0]
    assert "monthly:" in section
    assert "yearly:" in section
    # 49/58 = 84.5%, 9/58 = 15.5%
    assert "(84.5%)" in section
    assert "(15.5%)" in section


