def _minimal_log(**overrides) -> str:
    base = dict(
        seed=42,
        sim_days=180,
        initial_count=1,
        events=[_ev("sim_1", 0, "customer_created", tier="standard", cadence="month")],
        states={"sim_1": _state("sim_1")},
        state_counts=Counter({"active": 1}),
        tier_counts=Counter({"standard": 1}),
        cadence_counts=Counter({"month": 1}),
        generated_on="2026-05-05",
    )
    base.update(overrides)
    return format_run_log(**base)


def test_log_header_line_first():
    text = _minimal_log()
    first = text.splitlines()[0]
    assert first == (
        "# Generated by scripts/seeder/__main__.py on 2026-05-05. "
        "Regenerate via 'python -m scripts.seeder'."
    )


