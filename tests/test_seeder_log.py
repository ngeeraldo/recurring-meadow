from collections import Counter

from scripts.seeder import simulator
from scripts.seeder.__main__ import CustomerState
from scripts.seeder.run_log import format_event, format_run_log


def _state(sim_id: str, *, tier="standard", cadence="month", state="active") -> CustomerState:
    return CustomerState(
        sim_id=sim_id,
        stripe_customer_id=f"cus_{sim_id}",
        sub_id=f"sub_{sim_id}",
        clock_id=f"clk_{sim_id}",
        clock_day=180,
        current_tier=tier,
        current_cadence=cadence,
        current_state=state,
    )


def _ev(sim_id: str, day: int, type_: str, **payload) -> simulator.Event:
    return simulator.Event(day=day, sim_id=sim_id, type=type_, payload=payload)


