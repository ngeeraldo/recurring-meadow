def test_per_customer_block_creation_only_emits_no_further_events_line():
    text = _minimal_log()
    assert "─── sim_1  cus_sim_1  sub_sim_1 ───" in text
    assert "day   0: customer_created (standard, monthly)" in text
    assert "(no further events)" in text


def test_per_customer_block_with_upgrade_and_cancel():
    events = [
        _ev("sim_1", 0, "customer_created", tier="standard", cadence="month"),
        _ev("sim_1", 47, "tier_upgraded", **{"from": "standard", "to": "pro_plus"}),
        _ev("sim_1", 102, "canceled"),
    ]
    text = _minimal_log(events=events)
    assert "day   0: customer_created (standard, monthly)" in text
    assert "day  47: tier_upgraded (standard → pro_plus)" in text
    assert "day 102: canceled" in text
    assert "(no further events)" not in text


def test_per_customer_block_includes_stripe_ids_in_header():
    states = {
        "sim_1": CustomerState(
            sim_id="sim_1",
            stripe_customer_id="cus_ABC123",
            sub_id="sub_XYZ789",
            clock_id="clk_1",
            clock_day=180,
            current_tier="standard",
            current_cadence="month",
            current_state="active",
        )
    }
    text = _minimal_log(states=states)
    assert "─── sim_1  cus_ABC123  sub_XYZ789 ───" in text


def test_customers_sorted_by_numeric_suffix_not_lexicographically():
    """Regression: string sort produces sim_1, sim_10, sim_2, sim_20 — looks broken."""
    sim_ids = ["sim_1", "sim_2", "sim_10", "sim_20"]
    states = {sid: _state(sid) for sid in sim_ids}
    events = [_ev(sid, 0, "customer_created", tier="standard", cadence="month") for sid in sim_ids]
    text = _minimal_log(states=states, events=events, initial_count=4)

    positions = [text.index(f"─── {sid}  ") for sid in sim_ids]
    # Block for sim_1 must come before sim_2, which must come before sim_10, which before sim_20.
    assert positions == sorted(positions), (
        f"Expected numeric sort sim_1 < sim_2 < sim_10 < sim_20, got positions {positions}"
    )
